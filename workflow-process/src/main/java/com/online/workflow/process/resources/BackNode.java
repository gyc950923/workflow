package com.online.workflow.process.resources;


public class BackNode {

	private Integer backRangeType;
	private String nodeId;
	private String name;
	
	public Integer getBackRangeType() {
		return backRangeType;
	}
	public void setBackRangeType(Integer backRangeType) {
		this.backRangeType = backRangeType;
	}
	public String getNodeId() {
		return nodeId;
	}
	public void setNodeId(String nodeId) {
		this.nodeId = nodeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
